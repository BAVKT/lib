#include "libft.h"

void	*ft_memalloc(size_t n)
{
	char	*s;

	s = (char *)malloc(sizeof(n + 1));
	if (s == NULL || n > 2147483647 || n <= 0)
		return (NULL);
	ft_memset(s, '\0', n);
	s[n + 1] = '\0';
	return ((void *)s);
}
